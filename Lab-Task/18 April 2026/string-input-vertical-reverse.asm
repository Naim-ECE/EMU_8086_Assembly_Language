.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'ENTER STRING: $'
MSG2 DB 13,10,'VERTICAL DISPLAY:',13,10,'$'
MSG3 DB 13,10,'REVERSE DISPLAY:',13,10,'$'

STR DB 100 DUP('$')

.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX

LEA DX, MSG1
MOV AH, 09H
INT 21H

LEA DX, STR
MOV AH, 0AH
INT 21H

LEA DX, MSG2
MOV AH, 09H
INT 21H

MOV CL, STR+1
MOV CH, 0
LEA SI, STR+2

VERTICAL_LOOP:
MOV DL, [SI]
MOV AH, 02H
INT 21H

MOV DL, 13
INT 21H
MOV DL, 10
INT 21H

INC SI
LOOP VERTICAL_LOOP

LEA DX, MSG3
MOV AH, 09H
INT 21H

MOV CL, STR+1
MOV CH, 0
LEA SI, STR+2
ADD SI, CX
DEC SI

REVERSE_LOOP:
MOV DL, [SI]
MOV AH, 02H
INT 21H

DEC SI
LOOP REVERSE_LOOP

MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN
