.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "Enter a number: $"
RESULT_MSG DB 10, 13, "Factorial is: $"

.CODE
START:
MOV AX, @DATA
MOV DS, AX

LEA SI, MSG1
MOV AH, 09H
INT 21H

MOV AH, 01H
INT 21H
SUB AL, 30H
MOV AH, 0
    
CMP AX, 0
JE IS_ZERO
    
MOV CX, AX
MOV AX, 1
FACT:
MUL CX
LOOP FACT
JMP DISP

IS_ZERO:
MOV AX, 1

DISP:
    
MOV CX, AX

LEA DX, RESULT_MSG
MOV AH, 09H
INT 21H

    
MOV AX, CX
MOV BX, 10
MOV DX, 0
MOV CX, 0

PUSH_DIGITS:
MOV DX, 0
DIV BX
PUSH DX
INC CX
CMP AX, 0
JNE PUSH_DIGITS

PRINT_LOOP:
POP DX
ADD DL, 30H
MOV AH, 02H
INT 21H
LOOP PRINT_LOOP

MOV AH, 4CH
INT 21H
END START