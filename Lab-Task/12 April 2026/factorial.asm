.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "Enter a single digit number: $"

.CODE
START:
MOV AX, @DATA
MOV DS, AX

LEA SI, MSG1
MOV AH, 09H
INT 21H

MOV AH, 01H
INT 21H
    
SUB AL, 30H
MOV AH, 0
    
CMP AX, 0
JE IS_ZERO
    
MOV CX, AX
MOV AX, 1

FACT:
MUL CX
LOOP FACT
JMP RES

IS_ZERO:
MOV AX, 1

RES:
MOV CX, AX

END START
